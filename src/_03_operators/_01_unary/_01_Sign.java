package _03_operators._01_unary;

public class _01_Sign {

	public static void main(String[] args) {
		int num = 10;
		System.out.println("num: " + num);
		System.out.println("+num: " + +num);
		System.out.println("-num: " + -num);
		System.out.println("------------------");
		
		num = -10;
		System.out.println("num: " + num);
		System.out.println("+num: " + +num);
		System.out.println("-num: " + -num);
		System.out.println("------------------");
		
		num = -10;
		System.out.println("num: " + num);
		System.out.println("-num: " + -num);
		System.out.println("+num: " + +num);
	}

}
